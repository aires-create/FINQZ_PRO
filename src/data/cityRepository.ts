// Buscar cidades por termo
export const searchCities = (term: string): City[] => {
  if (!term || term.length < 2) return [];
  const lowerTerm = term.toLowerCase();
  return cityRepository.filter(c => 
    c.city.toLowerCase().includes(lowerTerm) ||
    c.state.toLowerCase().includes(lowerTerm)
  );
};

// Buscar cidade por nome
export const getCityByName = (cityName: string): City | undefined => {
  return cityRepository.find(c => c.city.toLowerCase() === cityName.toLowerCase());
};

// Listar todas as cidades
export const listCities = (): City[] => {
  return cityRepository;
};

// Listar estados únicos
export const listStates = (): string[] => {
  const states = new Set(cityRepository.map(c => c.state));
  return Array.from(states).sort();
};
